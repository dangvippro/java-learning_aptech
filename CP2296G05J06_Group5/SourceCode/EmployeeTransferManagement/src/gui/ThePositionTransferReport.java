    private void txtPosCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtPosCaretUpdate
        try {
            String query = "SELECT * FROM TransferPosition WHERE transferPositionName LIKE ?";
            ps = cn.prepareStatement(query);
            ps.setString(1, txtPos.getText() + "%");
            rs = ps.executeQuery();

            tblModel.getDataVector().removeAllElements();
            vtr = new Vector();
            while (rs.next()) {
                vtr = new Vector();
                vtr.add(rs.getString("transferPositionNumber"));
                vtr.add(rs.getString("transferPositionName"));
                vtr.add(rs.getString("allowanceLevel"));
                vtr.add(rs.getString("note"));
                tblModel.addRow(vtr);
            }
            if (vtr.isEmpty()) {
                tblModel.addRow(vtr);
            }
            tblPositionReport.setModel(tblModel);
        } catch (SQLException ex) {
                System.out.println("cc");
            Logger.getLogger(TheEmployeeMasterRecords.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtPosCaretUpdate

