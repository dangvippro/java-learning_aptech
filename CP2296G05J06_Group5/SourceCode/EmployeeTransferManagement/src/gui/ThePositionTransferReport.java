    public void LoadTable() {
        try {
            ps = cn.prepareStatement("SELECT * FROM TransferPosition");
            rs = ps.executeQuery();

            while (rs.next()) {
                vtr = new Vector();
                vtr.add(rs.getString("transferPositionNumber"));
                vtr.add(rs.getString("transferPositionName"));
                vtr.add(rs.getString("allowanceLevel"));
                vtr.add(rs.getString("note"));
                tblModel.addRow(vtr);
            }
            if (vtr.isEmpty()) {
                tblModel.addRow(vtr);
            }
            tblPositionReport.setModel(tblModel);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "SQLServer errors");
            Logger.getLogger(TheEmployeeMasterRecords.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

